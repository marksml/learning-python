# create a list with names
print("create and print a list of names:")
names = ["Alice", "Bob", "Charlie", "Diana"]
# print each name in the list
for name in names:
    print(name)

# print a newline for better readability
print()

# create a dictionary with ages
print("create and print a dictionary of names with age:")
ages = {"Alice": 30, "Bob": 25, "Charlie": 35, "Diana": 28}
# print each name with corresponding age
for name, age in ages.items():
    print(f"{name} is {age} years old.")

# print a newline for better readability
print()

# create a set with unique colors
print("create and print a set of colors:")
colors = {"red", "blue", "green", "yellow"}
# print each color in the set
for color in colors:
    print(color)    

# print a newline for better readability
print()

# create a tuple with coordinates
print("create and print a tuple of coordinates:")
coordinates = (10.0, 20.0)
