# create a list with names
names = ["Alice", "Bob", "Charlie", "Diana"]
# print each name in the list
for name in names:
    print(name)
# create a dictionary with ages
ages = {"Alice": 30, "Bob": 25, "Charlie": 35, "Diana": 28}
# print each name with corresponding age    
for name, age in ages.items():
    print(f"{name} is {age} years old.")

# create a set with unique colors
colors = {"red", "blue", "green", "yellow"}
# print each color in the set
for color in colors:
    print(color)    
